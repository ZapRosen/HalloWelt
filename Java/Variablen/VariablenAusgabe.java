package com.example.variablenausgabe;

public class VariablenAusgabe {
    public static void main(String[] args){
        int meiNumber = 33;
        short meiShort =333;
        long meiLong =4444;

        double meiDouble = 5.334;
        float meiFloat = 300.3f;

        char meiChar = 'y';
        boolean meiBoolean = false;

        byte meiByte = 127;

        System.out.println(meiNumber);
        System.out.println(meiShort);
        System.out.println(meiLong);
        System.out.println(meiDouble);
        System.out.println(meiFloat);
        System.out.println(meiChar);
        System.out.println(meiBoolean);
        System.out.println(meiByte);

    }
}
